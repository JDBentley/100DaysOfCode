#Create a band name generator.
#Take input of a pet name and town. Combine them for the result.
print('Welcome to the band name generator.')

#Getting inputs
pet_name = input("What's the name of your favorite pet? ")
town_name = input("What is the name of the town where you grew up? ")

#Adding the two strings together
band_name = pet_name + town_name

#Printing results
print(band_name)